package e201;

public class hola {
    
    public void holaFor() {
        for (int i = 0; i < 5; i++) {
            System.out.println("Hola");
        }
    }
    
    public void holaWhile() {
        int contador = 0;
        while (contador < 5) {
            System.out.println("Hola");
            contador++;
        }
    }
    
    public void holaDoWhile() {
        int contador2 = 0;
        do {
            System.out.println("Hola");
            contador2++;
        } while (contador2 < 5);
    }
    
    
    
}
